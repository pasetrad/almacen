    private void ListarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListarMouseClicked
        // TODO add your handling code here:
        pListar.setVisible(true);
        
        
        controladorLista = new ControladorLista();
        ArrayList<Producto> listProducto = new ArrayList<Producto>();
        try {
            listProducto = controladorLista.getListadoProducto();
        } catch (SQLException ex) {
            Logger.getLogger(Almacen.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Object[] fila=new Object[4];
        for(int x=0; x<listProducto.size(); x++){
            fila[0]=listProducto.get(x);//.getCodigo();
            fila[1]=listProducto.get(x).getNombreProducto();
            fila[2]=listProducto.get(x).getStock();
            fila[3]=listProducto.get(x).getPrecio();
            modelo.addRow(fila);
        }
        
    }//GEN-LAST:event_ListarMouseClicked

