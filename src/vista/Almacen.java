    private void bAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarActionPerformed
        // TODO add your handling code here:
        Producto producto = new Producto();
        
        producto.setNombreProducto(cNombreProducto.getText());
	producto.setStock(Integer.parseInt(cStock.getText()));
	producto.setPrecio(Integer.parseInt(cPrecio.getText()));
        ControladorAgregar controladorAgregar = new ControladorAgregar();
        boolean respuesta = controladorAgregar.agregarproducto(producto);
        if (respuesta==true){
            JOptionPane.showMessageDialog(null,"El producto ya está ingresado.");
        } else {
            JOptionPane.showMessageDialog(null,"El producto "+producto.getNombreProducto()+" se ha grabado correctamente.");
        }
        
    }//GEN-LAST:event_bAgregarActionPerformed

